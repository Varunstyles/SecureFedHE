def load_diabetes_datasets(
    num_clients:  int   = 5,
    alpha:        float = 0.3,    # low alpha = realistic non-IID hospitals
    batch_size:   int   = 16,
    test_split:   float = 0.2,
    seed:         int   = 42,
    node_id:      Optional[int] = None,
    verbose:      bool  = True,
) -> Tuple:
    """
    Load and partition the Pima diabetes dataset across N hospital nodes.

    Args:
        num_clients : number of hospital nodes
        alpha       : Dirichlet concentration (0.3 = realistic non-IID)
        batch_size  : training batch size
        test_split  : fraction held out as global test set
        seed        : reproducibility seed
        node_id     : if set, return only this node's DataLoader
        verbose     : print partition summary

    Returns:
        If node_id is None:
            (train_loaders, test_loader, mean, std)
        If node_id is set:
            (my_train_loader, test_loader, mean, std)
    """
    rng = np.random.default_rng(seed)

    data = _RAW_DATA.copy()
    rng.shuffle(data)

    X = data[:, :8]
    y = data[:, 8].astype(int)

    # Global train/test split
    n_test = int(len(X) * test_split)
    X_test,  y_test  = X[:n_test],  y[:n_test]
    X_train, y_train = X[n_test:],  y[n_test:]

    # Compute normalisation stats from training data only
    mean = X_train.mean(axis=0)
    std  = X_train.std(axis=0) + 1e-8

    # Non-IID partition
    client_idx = _partition_noniid_dirichlet(y_train, num_clients, alpha, seed)

    # Build per-client datasets
    train_loaders = []
    for i, idx in enumerate(client_idx):
        ds = DiabetesDataset(X_train[idx], y_train[idx], mean, std)
        loader = DataLoader(
            ds,
            batch_size=min(batch_size, len(ds)),
            shuffle=True,
            num_workers=0,      # Windows-safe
            drop_last=False,
        )
        train_loaders.append(loader)

    # Global test loader
    test_ds     = DiabetesDataset(X_test, y_test, mean, std)
    test_loader = DataLoader(
        test_ds,
        batch_size=64,
        shuffle=False,
        num_workers=0,          # Windows-safe
    )

    if verbose:
        print("\n" + "="*60)
        print("  SecureFedHE — Federated Hospital Data Partition")
        print("="*60)
        print(f"  Total patients : {len(data)}")
        print(f"  Training set   : {len(X_train)} patients")
        print(f"  Test set       : {len(X_test)} patients")
        print(f"  Partition      : Non-IID Dirichlet (α={alpha})")
        print(f"  Hospitals      : {num_clients}")
        print("-"*60)
        for i, idx in enumerate(client_idx):
            h = HOSPITAL_PROFILES.get(i, {"name": f"Hospital {i}"})
            local_y = y_train[idx]
            diabetic = local_y.sum()
            total    = len(local_y)
            rate     = diabetic / total * 100 if total > 0 else 0
            print(f"  🏥 {h['name']}")
            print(f"     Patients: {total:>4}  |  Diabetic: {diabetic} ({rate:.1f}%)")
        print("="*60 + "\n")

    if node_id is not None:
        return train_loaders[node_id % num_clients], test_loader, mean, std

    return train_loaders, test_loader, mean, std


